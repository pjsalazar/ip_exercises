package com.salazar.peter.lesson1;

public class DataTypeTest {
	// Write a class that creates and stores a value for each of the following data types: int, double, float, char, boolean
	// Output these values to the display

	public static void main (String[] args) {
		int iTest = 12; 
		double dTest = 2.71828;
		float fTest = 3.1415f;
		char cTest = 'c';
		boolean bTest = true;
		
		System.out.println(iTest);
		System.out.println(dTest);
		System.out.println(fTest);
		System.out.println(cTest);
		System.out.println(bTest);
	}
}